    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Date date = new Date();
        if (name.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name can not be Empty");
        } else if (startDate.getDate().before(date) && !hasProject.getText().equals("True")) {
            JOptionPane.showMessageDialog(this, "You can't Select A date in the Past");
        } else if (endDate.getDate().before(startDate.getDate())) {
            JOptionPane.showMessageDialog(this, "You can't Pick an End Date In the past of Start Date");
        } else if (!active.isSelected() && !onHold.isSelected() && !finished.isSelected()) {
            JOptionPane.showMessageDialog(this, "Set Status");
        } else {
            try {
                Project pj = new Project();
                if (hasProject.getText().equals("True")) {
                    Integer theId = Integer.parseInt(searchEmp.getText());
                    pj.setProjectId(theId);
                }
                pj.setName(name.getText());
                pj.setDescription(desc.getText());
                pj.setStartDate(startDate.getDate());
                pj.setEndDate(endDate.getDate());
                if (active.isSelected()) {
                    pj.setStatus(active.getText());
                } else if (onHold.isSelected()) {
                    pj.setStatus(onHold.getText());
                } else {
                    pj.setStatus(finished.getText());
                }

                Registry theReg = LocateRegistry.getRegistry("127.0.0.1", 8001);
                ProjectServices service = (ProjectServices) theReg.lookup("project");
                Project pjObj = service.saveProject(pj);

                if (pjObj != null) {
                    JOptionPane.showMessageDialog(this, "Project Saved");
                    name.setText("");
                    desc.setText("");
                    startDate.setDate(null);
                    endDate.setDate(null);
                    status.clearSelection();
                    searchEmp.setText("");
                    hasProject.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Something wrong");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

