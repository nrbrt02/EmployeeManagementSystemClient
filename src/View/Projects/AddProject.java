    private void submitSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitSearchActionPerformed
        if (searchEmp.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Search can't be Empity");
        } else if (!searchEmp.getText().matches("^[0-9]*$")) {
            JOptionPane.showMessageDialog(this, "Only search by Id");
        } else {
            try {
                Project pj = new Project();
                Integer theId = Integer.parseInt(searchEmp.getText());
                pj.setProjectId(theId);

                Registry theReg = LocateRegistry.getRegistry("127.0.0.1", 8001);
                ProjectServices service = (ProjectServices) theReg.lookup("project");
                Project project = service.searchById(pj);

                if (project == null) {
                    JOptionPane.showMessageDialog(this, "No Results");
                } else {
                    hasProject.setText("True");
                    name.setText(project.getName());
                    desc.setText(project.getDescription());
                    startDate.setDate(project.getStartDate());
                    endDate.setDate(project.getEndDate());

                    if (project.getStatus().equals("Active")) {
                        active.setSelected(true);
                    } else if (project.getStatus().equals("On Hold")) {
                        onHold.setSelected(true);
                    } else {
                        finished.setSelected(true);
                    }

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_submitSearchActionPerformed

    private void cancel1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancel1ActionPerformed

        if (!hasProject.getText().equals("True")) {
            JOptionPane.showMessageDialog(this, "Search Project First");
        } else {
            int choice = JOptionPane.showConfirmDialog(this, "Are you sure to delete");
            if (choice == JOptionPane.YES_OPTION) {
                try {
                    Project pj = new Project();
                    Integer theId = Integer.parseInt(searchEmp.getText());
                    pj.setProjectId(theId);

                    Registry theReg = LocateRegistry.getRegistry("127.0.0.1", 8001);
                    ProjectServices service = (ProjectServices) theReg.lookup("project");
                    Project project = service.deleteProject(pj);

                    if (project != null) {
                        JOptionPane.showMessageDialog(this, "Project Deleted");
                        name.setText("");
                        desc.setText("");
                        startDate.setDate(null);
                        endDate.setDate(null);
                        status.clearSelection();
                        searchEmp.setText("");
                        hasProject.setText("");
                    } else {
                        JOptionPane.showMessageDialog(this, "Something wrong");
                    }
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_cancel1ActionPerformed

